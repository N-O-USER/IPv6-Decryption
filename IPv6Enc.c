//IPv6 shellcode should be a multiple of 16 but a padding func can be made if a multiple of 16 isn't met
char* GenerateIpv6(unsigned char* bytes) {
	char* output = HeapAlloc(GetProcessHeap(), HEAP_ZERO_MEMORY, 128);
	if (!output) return NULL; 

	// generate the IPv6 addr and save it to output then print output
	sprintf_s(output, 128, "%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X:%02X%02X",
		bytes[0], bytes[1], bytes[2], bytes[3], bytes[4], bytes[5], 
		bytes[6], bytes[7], bytes[8], bytes[9], bytes[10], bytes[11], 
		bytes[12], bytes[13], bytes[14], bytes[15]);

	return output;
}

// Generate IPv6 output of shell and have a *p and size of buffer
BOOL IPv6Output(unsigned char* pShellCode, SIZE_T ShellCodeSize) {
	SIZE_T counter = 0; 

	if (pShellCode == NULL || ShellCodeSize == 0 || ShellCodeSize % 16 != 0) {
		return FALSE;
	}
	//printf("[+] char* Ipv6Array[%d] = { \n\t", (int)(ShellCodeSize / 16));
	printf("char* Ipv6Array[%zu] = { \n\t", ShellCodeSize / 16);
	//printf("[DEBUG] pShellCode: %p, size: %zu\n", pShellCode, ShellCodeSize);

	// Iterate through *pShellCode array of 16 byte segments and output them
	for (SIZE_T i = 0; i < ShellCodeSize; i += 16) {
		char* IPAddress = GenerateIpv6(&pShellCode[i]);
		if (!IPAddress) return FALSE;

		if (i == ShellCodeSize - 16) {
			printf("\"%s\"", IPAddress);
		}
		else {
			printf("\"%s\", ", IPAddress);
		}

		counter++;
		// new line every 4 entries for readability
		if (counter % 4 == 0 && i != ShellCodeSize - 16) {
			printf("\n\t");
		}

		HeapFree(GetProcessHeap(), 0, IPAddress); 
		IPAddress = NULL; 
	}
	printf("\n};\n\n");
	return TRUE;
} 


SIZE_T PadShellcode(unsigned char** pShellcode, SIZE_T ShellcodeSize) {
	SIZE_T paddedSize = (ShellcodeSize + 15) & ~15; // round up to next multiple of 16

	// Allocate new mem block for padded size and copy over
	unsigned char* pUpdatedShellCode = HeapAlloc(GetProcessHeap(), HEAP_ZERO_MEMORY, paddedSize);
	if (!pUpdatedShellCode) return 0; 

	memcpy(pUpdatedShellCode, *pShellcode, ShellcodeSize); 
	// write bytes after og data, NOP instruction to write, how many NOP bytes are needed to fill the gap
	memset(pUpdatedShellCode + ShellcodeSize, 0x90, paddedSize - ShellcodeSize);

	HeapFree(GetProcessHeap(), 0, *pShellcode); 
	*pShellcode = pUpdatedShellCode; 
	return paddedSize;
}

