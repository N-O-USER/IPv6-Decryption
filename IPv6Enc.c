int main() {
	// Copy static buf into heap so we can realloc for padding
	SIZE_T ShellcodeSize = sizeof(buf);
	unsigned char* pShellcode = HeapAlloc(GetProcessHeap(), HEAP_ZERO_MEMORY, ShellcodeSize);
	if (!pShellcode) 
		return -1;
	memcpy(pShellcode, buf, ShellcodeSize);

	// Pad to multiple of 16 if needed
	if (ShellcodeSize % 16 != 0) {
		ShellcodeSize = PadShellcode(&pShellcode, ShellcodeSize);
		if (ShellcodeSize == 0) {
			printf("[!] failed to pad shellcode\n");
			HeapFree(GetProcessHeap(), 0, pShellcode);
			pShellcode = NULL;
			return -1;
		}
	} 

	// Generate and print IPv6 representation of the shellcode
	if (!IPv6Output(pShellcode, ShellcodeSize)) {
		printf("[!] failed to encode shellcode: %d\n", GetLastError());
		HeapFree(GetProcessHeap(), 0, pShellcode);
		return -1;
	}

	HeapFree(GetProcessHeap(), 0, pShellcode);
	printf("[#] press enter to quit\n");
	(void)getchar();
	return 0;
}